$(document).ready(function () {
      $("#element_4").change(function() {
var x = $('#element_4 option:selected').text();
var a = "H5085";
var b = "B6767";
var c = "L8090";
if (x==a) {
 $("#element_5").val("Phones");
} else if (x==b) {
 $("#element_5").val("Travel Expense");
} else if (x==c) {
 $("#element_5").val("Repair and Maintenace");
}
     });
   });
